    private void showActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showActionPerformed
        DefaultTableModel tm = new DefaultTableModel();
        tm=(DefaultTableModel)table1.getModel();

        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/busticket", "root", "");
            Statement st =(Statement)con.createStatement();
            String query="Select seatno from sales_Product";
            rs=st.executeQuery(query);

            while(rs.next())
            {
                //String name=rs.getString("Name");
                //String phoneno =rs.getString("PhoneNo");
                String seatno =rs.getString("SeatNo");
                table1.setModel(tm);
                tm.addRow(new Object[]{seatno});

            }

            JOptionPane.showMessageDialog(null,"Data Showed Successfully");

        }

        catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e); 
        } catch (ClassNotFoundException e) {
            JOptionPane.showMessageDialog(this, e); 
        } 
    }//GEN-LAST:event_showActionPerformed

