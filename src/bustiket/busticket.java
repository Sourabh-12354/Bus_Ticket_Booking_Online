    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if(txtName.getText().equals("")||txtPhone.getText().equals("")||txtDate.getText().equals("")||txtSeat.getText().equals("")||txtTo.getText().equals("")||txtFrom.getText().equals("")||txtTime.getText().equals("")||txtPrice.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "Please Enter All Data");
        }
        else
        {
            try
            {
                Class.forName("com.mysql.jdbc.Driver");
                con = DriverManager.getConnection("jdbc:mysql://localhost/busticket", "root", "");
                String sql="INSERT INTO sales_product(name,phoneno,seatno,date,ending_point,starting_point,time,price) VALUES(?,?,?,?,?,?,?,?)";
                pst = con.prepareStatement(sql);
                pst.setString(1,txtName.getText());
                pst.setString(2,txtPhone.getText());
                pst.setString(3,txtSeat.getText());
                pst.setString(4,txtDate.getText());
                pst.setString(5,txtTo.getText());
                pst.setString(6,txtFrom.getText());
                pst.setString(7,txtTime.getText());
                pst.setInt(8,Integer.parseInt(txtPrice.getText()));
                pst.execute();
                JOptionPane.showMessageDialog(this, "Purchased Succesfully!!!");
                txtName.setText("");
                txtPhone.setText("");
                txtSeat.setText("");
                txtDate.setText("");
                txtTo.setText("");
                txtFrom.setText("");
                txtTime.setText("");
                txtPrice.setText("");
            }
            catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(busticket.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

